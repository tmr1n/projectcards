import type { Modifier } from '@dnd-kit/core'

// Модификатор dnd-kit — функция, которая правит трансформацию перетаскиваемого
// элемента на каждый кадр. Здесь обнуляем горизонтальный сдвиг (x: 0), поэтому
// карточка двигается ТОЛЬКО по вертикали.
//
// Зачем: список карточек вертикальный, но без ограничителя dnd-kit тянет элемент
// за курсором и вбок — трансформация вылезает за контейнер и растягивает страницу
// влево-вправо. С x: 0 этого не происходит.
export const restrictToVerticalAxis: Modifier = ({ transform }) => ({
	...transform,
	x: 0
})
