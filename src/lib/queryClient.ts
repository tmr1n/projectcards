// ЧТО ТАКОЕ TANSTACK QUERY?
// ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
//
//  TanStack Query — библиотека для управления СЕРВЕРНЫМ состоянием.
//
//  Разберём разницу между двумя типами состояния:
//
//  ┌──────────────────────────────┬─────────────────────────────────┐
//  │  КЛИЕНТСКОЕ состояние        │  СЕРВЕРНОЕ состояние             │
//  ├──────────────────────────────┼─────────────────────────────────┤
//  │  "тема: светлая/тёмная"      │  данные с бэкенда               │
//  │  "меню открыто/закрыто"      │  профиль пользователя           │
//  │  "залогинен ли пользователь" │  список карточек/колод          │
//  │                              │  статистика обучения            │
//  ├──────────────────────────────┼─────────────────────────────────┤
//  │  Хранит: Zustand             │  Хранит: TanStack Query         │
//  └──────────────────────────────┴─────────────────────────────────┘
//
//  Zustand и TanStack Query — НЕ конкуренты, а партнёры.
//  Каждый делает своё дело. Они используются вместе.
//
// ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
// КАК БЫ МЫ ЖИЛИ БЕЗ TANSTACK QUERY?
// ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
//
//  Представь: нам нужно показать профиль пользователя.
//
//  БЕЗ TanStack Query (старый способ):
//
//    function ProfilePage() {
//      const [user, setUser] = useState(null)       // хранить данные
//      const [isLoading, setIsLoading] = useState(false) // лоадер
//      const [error, setError] = useState(null)     // ошибка
//
//      useEffect(() => {
//        setIsLoading(true)
//        fetch('/api/profile')
//          .then(res => res.json())
//          .then(data => setUser(data))
//          .catch(err => setError(err.message))
//          .finally(() => setIsLoading(false))
//      }, [])
//
//      if (isLoading) return <Spinner />
//      if (error) return <ErrorBanner message={error} />
//      return <div>{user.name}</div>
//    }
//
//  Проблемы этого подхода:
//    ❌ 10 строк только чтобы сделать один запрос
//    ❌ Нет кеша: каждый раз при заходе на страницу — новый запрос
//    ❌ Если компонент размонтируется до ответа — утечка памяти
//    ❌ Два компонента хотят одни данные → два одинаковых запроса
//    ❌ Трудно синхронизировать данные после мутации (обновления)
//
//  С TanStack Query:
//
//    function ProfilePage() {
//      const { data, isLoading, error } = useQuery({
//        queryKey: ['profile'],
//        queryFn: () => apiFetch('/profile')
//      })
//
//      if (isLoading) return <Spinner />
//      if (error) return <ErrorBanner />
//      return <div>{data.name}</div>
//    }
//
//    ✅ 3 строки вместо 10
//    ✅ Кеш: данные сохранены 5 минут, повторный запрос не нужен
//    ✅ Нет утечек памяти (автоматически отменяет запросы)
//    ✅ Два компонента = один запрос (если queryKey одинаковый)
//    ✅ После мутации — инвалидируй ключ, данные обновятся сами
//
// ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
// ЗАЧЕМ ЭТО НУЖНО В ЭТОМ ПРОЕКТЕ КОНКРЕТНО?
// ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
//
//  ProjectCards — приложение для изучения материала с карточками.
//  Когда бэкенд будет готов, нам понадобится:
//
//    📄 Профиль пользователя        GET /profile
//    📦 Список колод карточек        GET /decks
//    🃏 Карточки внутри колоды       GET /decks/:id/cards
//    📊 Статистика обучения          GET /stats
//    ✏️  Создать/удалить карточку    POST/DELETE /cards
//
//  Все эти данные — серверное состояние.
//  Zustand не предназначен для этого (он для UI-состояния).
//  TanStack Query — именно для этого.
//
// ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
// СХЕМА СВЯЗИ ВСЕХ ЧАСТЕЙ
// ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
//
//   ┌─────────────────────────────────────────────────────┐
//   │                  React компонент                     │
//   │  const { data } = useQuery({ queryKey, queryFn })   │
//   └────────────────────┬────────────────────────────────┘
//                        │ обращается к
//                        ▼
//   ┌─────────────────────────────────────────────────────┐
//   │              TanStack Query Cache                    │
//   │  ┌─────────────────────────────────────────────┐   │
//   │  │ 'profile'     → { name: 'Артём', email: ... }│   │
//   │  │ 'decks'       → [{ id: 1, title: 'React' }] │   │
//   │  │ 'decks', '1'  → { cards: [...] }             │   │
//   │  └─────────────────────────────────────────────┘   │
//   │                    ↑ кешировано 5 минут              │
//   └────────────────────┬────────────────────────────────┘
//                        │ если данных нет или устарели
//                        ▼
//   ┌─────────────────────────────────────────────────────┐
//   │                  queryFn                             │
//   │  () => apiFetch('/profile', { token })              │
//   └────────────────────┬────────────────────────────────┘
//                        │
//                        ▼
//   ┌─────────────────────────────────────────────────────┐
//   │               lib/api.ts → apiFetch                  │
//   │  (тот же клиент что и в server-actions)             │
//   └────────────────────┬────────────────────────────────┘
//                        │ HTTP запрос
//                        ▼
//                    Backend API
//
//  После успешного ответа данные сохраняются в кеше.
//  Следующий вызов useQuery('profile') — данные из кеша, без запроса.
//
// ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
// ЖИЗНЕННЫЙ ЦИКЛ ДАННЫХ В КЕШЕ
// ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
//
//  fresh (свежие)   — только что получены, повторного запроса нет
//       ↓  staleTime (5 мин по умолчанию)
//  stale (устарели) — при следующем использовании будет refetch
//       ↓  gcTime (10 мин по умолчанию, раньше называлось cacheTime)
//  удалены из памяти
//
//  Дополнительные refetch-триггеры (по умолчанию включены):
//    — пользователь вернулся на вкладку (refetchOnWindowFocus)
//    — восстановилась сеть (refetchOnReconnect)
//
// ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

import { QueryClient } from '@tanstack/react-query'

// ─────────────────────────────────────────────────────────────
// QueryClient — "мозг" TanStack Query
// ─────────────────────────────────────────────────────────────
//
// QueryClient управляет:
//   — кешем (хранит все полученные данные)
//   — запросами (когда делать fetch, когда использовать кеш)
//   — состоянием (isLoading, isError, isFetching для каждого запроса)
//
// Один экземпляр на всё приложение.
// Передаётся в <QueryClientProvider> (см. providers/QueryProvider.tsx).
// Оттуда useQuery/useMutation получают к нему доступ через контекст.

export const queryClient = new QueryClient({
	defaultOptions: {
		queries: {
			// ── staleTime ──────────────────────────────────────────
			// Как долго данные считаются "свежими" (в миллисекундах).
			// В это время повторный запрос НЕ делается.
			//
			// 5 * 60 * 1000 = 5 минут
			//
			// Пример: открыл профиль → данные загрузились.
			// Перешёл на другую страницу и вернулся через 3 минуты —
			// данные из кеша, запроса нет. Вернулся через 7 минут —
			// данные устарели, будет тихий фоновый refetch.
			staleTime: 5 * 60 * 1000,

			// ── gcTime ─────────────────────────────────────────────
			// Как долго НЕИСПОЛЬЗУЕМЫЕ данные хранятся в памяти.
			// "Неиспользуемые" = нет ни одного компонента с этим queryKey.
			//
			// 10 * 60 * 1000 = 10 минут
			//
			// Пример: закрыл страницу профиля → данные ещё 10 минут
			// в кеше. Вернулся через 8 минут — данные ещё есть,
			// покажутся мгновенно (и пойдёт фоновый refetch если stale).
			gcTime: 10 * 60 * 1000,

			// ── retry ──────────────────────────────────────────────
			// Сколько раз повторять запрос при ошибке.
			// По умолчанию в TanStack Query = 3. Мы ставим 1:
			// лучше быстро показать ошибку, чем ждать 3 попытки.
			retry: 1,

			// ── refetchOnWindowFocus ────────────────────────────────
			// Делать ли refetch когда пользователь возвращается на вкладку.
			//
			// true  = данные всегда свежие (хорошо для critical данных)
			// false = не надоедать лишними запросами (хорошо для статики)
			//
			// Ставим false — при желании можно включить для конкретного
			// запроса через { refetchOnWindowFocus: true } в useQuery.
			refetchOnWindowFocus: false,
		},
	},
})
