// ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
// ПРИНЦИП: каждый endpoint — свой хук
// ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
//
//  Мы не вызываем useQuery прямо в компоненте.
//  Вместо этого создаём кастомный хук-обёртку здесь,
//  а компонент вызывает его.
//
//  Зачем?
//    — queryKey и queryFn прописаны один раз (не дублируются)
//    — логика выборки данных отделена от UI-компонента
//    — легко использовать в нескольких компонентах
//
//  Структура папки hooks/queries/:
//    useProfile.ts         ← GET /profile
//    useDecks.ts           ← GET /decks
//    useDeck.ts            ← GET /decks/:id
//    useCards.ts           ← GET /decks/:id/cards
//
// ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
// ЧТО ТАКОЕ queryKey?
// ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
//
//  queryKey — уникальный идентификатор запроса в кеше.
//  Это массив: ['profile'], ['decks'], ['decks', '42']
//
//  По нему TanStack Query:
//    — находит данные в кеше
//    — понимает когда инвалидировать (обновить) данные
//
//  Правило: queryKey должен включать ВСЕ переменные от которых
//  зависит запрос. Разные id = разные ключи = разные записи в кеше.
//
//    useDecks()          → ['decks']          — список всех колод
//    useDeck('42')       → ['decks', '42']    — конкретная колода
//    useDeck('99')       → ['decks', '99']    — другая колода (свой кеш)
//
// ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
// ЧТО ТАКОЕ queryFn?
// ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
//
//  queryFn — функция которая делает реальный запрос к бэкенду.
//  TanStack Query вызывает её когда данные нужны и их нет в кеше.
//
//  Она должна:
//    — вернуть Promise с данными при успехе
//    — выбросить ошибку при неудаче (TanStack Query её поймает)
//
//  Здесь мы используем apiFetch из lib/api.ts — тот же клиент
//  что и в server-actions. Разница: здесь вызов на КЛИЕНТЕ.
//
// ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
// СВЯЗЬ С АВТОРИЗАЦИЕЙ (Zustand + TanStack Query вместе)
// ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
//
//  Бэкенд требует токен для /profile. Где его взять?
//  В authStore! Оба инструмента живут рядом.
//
//  useProfile():
//    1. Читает токен из authStore (Zustand)
//    2. Передаёт токен в queryFn
//    3. queryFn вызывает apiFetch с токеном
//    4. Данные кешируются под ключом ['profile']
//
//    enabled: !!accessToken — запрос делается ТОЛЬКО если токен есть.
//    Если пользователь не залогинен — запроса не будет.
//
// ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
// КАК ИСПОЛЬЗОВАТЬ В КОМПОНЕНТЕ:
// ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
//
//  'use client'
//  import { useProfile } from '@/hooks/queries/useProfile'
//
//  function ProfilePage() {
//    const { data, isLoading, isError } = useProfile()
//
//    if (isLoading) return <Skeleton />
//    if (isError)   return <p>Не удалось загрузить профиль</p>
//
//    return <div>{data.username}</div>
//  }
//
// ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

import { useQuery } from '@tanstack/react-query'
import { apiFetch } from '@/lib/api'
import { useAuthStore } from '@/store/authStore'
import type { IUser } from '@/shared/types/auth.types'

// ─────────────────────────────────────────────────────────────
// QUERY KEYS — константы для ключей кеша
// ─────────────────────────────────────────────────────────────
//
// Выносим ключи в константы чтобы:
//   1. Не опечататься: 'profill' вместо 'profile'
//   2. При инвалидации использовать тот же ключ
//
// Пример инвалидации после обновления профиля:
//   queryClient.invalidateQueries({ queryKey: QUERY_KEYS.profile })
//   → TanStack Query сделает refetch, компонент обновится с новыми данными

export const QUERY_KEYS = {
	profile: ['profile'] as const,
	// Добавляй ключи по мере роста приложения:
	// decks: ['decks'] as const,
	// deck: (id: string) => ['decks', id] as const,
	// cards: (deckId: string) => ['decks', deckId, 'cards'] as const,
} as const

// ─────────────────────────────────────────────────────────────
// useProfile — хук для получения профиля пользователя
// ─────────────────────────────────────────────────────────────
//
// Возвращает объект со всеми полями useQuery:
//
//  data        — данные (IUser) или undefined пока грузится
//  isLoading   — true во время первого запроса (данных ещё нет)
//  isFetching  — true во время ЛЮБОГО запроса (включая фоновый refetch)
//  isError     — true если запрос завершился ошибкой
//  error       — объект ошибки (ApiError или другая)
//  refetch     — функция для ручного запуска запроса
//
//  Разница isLoading vs isFetching:
//    isLoading  = нет данных И идёт запрос → показывай полный Skeleton
//    isFetching = идёт запрос (данные могут уже быть старые) → маленький спиннер

export function useProfile() {
	// Достаём токен из Zustand store
	// useAuthStore здесь — это клиентский хук, поэтому компонент
	// должен быть 'use client'
	const accessToken = useAuthStore(state => state.accessToken)

	return useQuery({
		// Ключ в кеше. По нему TanStack Query хранит и находит данные.
		queryKey: QUERY_KEYS.profile,

		// Функция которая делает реальный запрос.
		// Вызывается когда данных нет в кеше или они устарели.
		queryFn: () =>
			apiFetch<IUser>('/profile', {
				// Передаём токен — apiFetch добавит заголовок Authorization
				token: accessToken!,
			}),

		// enabled: false — запрос не выполняется.
		// enabled: true  — запрос выполняется как обычно.
		//
		// !!accessToken преобразует строку/null в boolean:
		//   'eyJ...' → true  (токен есть → делаем запрос)
		//   null     → false (нет токена → не делаем запрос)
		//
		// Это защита: незалогиненный пользователь не получит 401-ошибок.
		enabled: !!accessToken,
	})
}
