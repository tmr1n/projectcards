// ============================================================
// api.config.ts — конфигурация API
// ============================================================
//
// Зачем отдельный файл?
// Чтобы при смене backend URL не искать его по всему проекту.
// Меняешь одну строку здесь — меняется везде.
//
// Переменные окружения (.env):
// ─────────────────────────────────────────────────────────
// В корне проекта создай файл .env.local (для разработки):
//
//   API_URL=http://localhost:8000/api/v1
//
// В продакшене (.env или через хостинг):
//
//   API_URL=https://api.yourdomain.com/api/v1
//
// ВАЖНО: без NEXT_PUBLIC_ префикса → переменная доступна
// только на сервере (в Server Actions). В браузер НЕ утекает.
// Это правильно — URL бэкенда не должен светиться клиенту.
// ─────────────────────────────────────────────────────────

// Fallback 'http://localhost:8000/api/v1' — на случай если
// .env.local не создан (чтобы приложение хотя бы запустилось).
export const API_BASE_URL =
	process.env.API_URL ?? 'http://localhost:8000/api/v1'
